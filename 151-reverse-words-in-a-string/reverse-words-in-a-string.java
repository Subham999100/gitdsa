class Solution {
    public String reverseWords(String s) {
        StringBuilder sb=new StringBuilder();
        int i=s.length()-1;;
        while(i>=0){
            while(i>=0 && s.charAt(i)==' '){
                i--;
            }
            if(i<0){
                break;
            }
            int r=i;
            while(i>=0 && s.charAt(i)!=' '){
                i--;
            }
            int l=i+1;
            sb.append(s.substring(l,r+1));
            sb.append(" ");
        } 
        return sb.toString().trim();
    }
}